public class datatype {
    public static void main(String args[])
    {   
        int a=22;
        char b='a';
        long c=100000L;
        float d=3499f;
        short e=45;
        double f=785;
        boolean g=true;
        System.out.println("value of int a="+a);
        System.out.println("value of char b="+b);
        System.out.println("value of long c="+c);
        System.out.println("value of float d="+d);
        System.out.println("value of short e="+e);
        System.out.println("value of double f="+f);
        System.out.println("value of boolean g="+g);

    }
    
}
